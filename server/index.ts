import express, { type Request, Response, NextFunction } from "express";
import session from "express-session";
import connectPgSimple from "connect-pg-simple";
import { registerRoutes } from "./routes";
import { serveStatic } from "./static";
import { createServer } from "http";
import { seedDatabase } from "./seed";
import { pool } from "./db";

const app = express();
const httpServer = createServer(app);

// Trust Replit's reverse proxy so secure cookies work in production
app.set("trust proxy", 1);

declare module "http" {
  interface IncomingMessage {
    rawBody: unknown;
  }
}

app.use(
  express.json({
    verify: (req, _res, buf) => {
      req.rawBody = buf;
    },
  }),
);

app.use(express.urlencoded({ extended: false }));

// Ensure session table exists (connect-pg-simple's createTableIfMissing can
// silently fail in production; create it explicitly to be safe)
pool.query(`
  CREATE TABLE IF NOT EXISTS "session" (
    "sid" varchar NOT NULL COLLATE "default",
    "sess" json NOT NULL,
    "expire" timestamp(6) NOT NULL,
    CONSTRAINT "session_pkey" PRIMARY KEY ("sid") NOT DEFERRABLE INITIALLY IMMEDIATE
  );
  CREATE INDEX IF NOT EXISTS "IDX_session_expire" ON "session" ("expire");
  ALTER TABLE IF EXISTS events ADD COLUMN IF NOT EXISTS end_date timestamp;
  ALTER TABLE IF EXISTS events ADD COLUMN IF NOT EXISTS is_internal boolean NOT NULL DEFAULT false;
  ALTER TABLE IF EXISTS events ADD COLUMN IF NOT EXISTS agenda text;
  ALTER TABLE IF EXISTS events ADD COLUMN IF NOT EXISTS program_pdf text;
  ALTER TABLE IF EXISTS events ADD COLUMN IF NOT EXISTS program_pdf_public boolean NOT NULL DEFAULT true;
  ALTER TABLE IF EXISTS subscribers ADD COLUMN IF NOT EXISTS birthday text;
  ALTER TABLE IF EXISTS subscribers ADD COLUMN IF NOT EXISTS referred_by_member_id INTEGER;
  ALTER TABLE IF EXISTS subscribers ADD COLUMN IF NOT EXISTS confirm_token text;
  ALTER TABLE IF EXISTS subscribers ADD COLUMN IF NOT EXISTS confirmed_at timestamp;
  CREATE UNIQUE INDEX IF NOT EXISTS subscribers_confirm_token_unique ON subscribers(confirm_token) WHERE confirm_token IS NOT NULL;
  CREATE TABLE IF NOT EXISTS "settings" (
    "key" text PRIMARY KEY,
    "value" text NOT NULL,
    "updated_at" timestamp NOT NULL DEFAULT now()
  );
  CREATE TABLE IF NOT EXISTS event_photos (
    id SERIAL PRIMARY KEY,
    event_id INTEGER NOT NULL REFERENCES events(id) ON DELETE CASCADE,
    filename TEXT NOT NULL,
    caption TEXT,
    uploaded_at TIMESTAMP NOT NULL DEFAULT now()
  );
`).then(() => {
  console.log("[session] table ready");
}).catch((err) => {
  console.error("[session] table creation error:", err);
});

const PgSession = connectPgSimple(session);

app.use(
  session({
    store: new PgSession({
      pool,
      tableName: "session",
      createTableIfMissing: false,
    }),
    secret: process.env.SESSION_SECRET || "fallback-secret-change-me",
    resave: false,
    saveUninitialized: false,
    cookie: {
      httpOnly: true,
      secure: process.env.NODE_ENV === "production",
      sameSite: "lax",
      maxAge: 7 * 24 * 60 * 60 * 1000,
    },
  })
);

export function log(message: string, source = "express") {
  const formattedTime = new Date().toLocaleTimeString("en-US", {
    hour: "numeric",
    minute: "2-digit",
    second: "2-digit",
    hour12: true,
  });

  console.log(`${formattedTime} [${source}] ${message}`);
}

app.use((req, res, next) => {
  const start = Date.now();
  const path = req.path;
  let capturedJsonResponse: Record<string, any> | undefined = undefined;

  const originalResJson = res.json;
  res.json = function (bodyJson, ...args) {
    capturedJsonResponse = bodyJson;
    return originalResJson.apply(res, [bodyJson, ...args]);
  };

  res.on("finish", () => {
    const duration = Date.now() - start;
    if (path.startsWith("/api")) {
      let logLine = `${req.method} ${path} ${res.statusCode} in ${duration}ms`;
      if (capturedJsonResponse) {
        logLine += ` :: ${JSON.stringify(capturedJsonResponse)}`;
      }

      log(logLine);
    }
  });

  next();
});

(async () => {
  await registerRoutes(httpServer, app);
  await seedDatabase();

  app.use((err: any, _req: Request, res: Response, next: NextFunction) => {
    const status = err.status || err.statusCode || 500;
    const message = err.message || "Internal Server Error";

    console.error("Internal Server Error:", err);

    if (res.headersSent) {
      return next(err);
    }

    return res.status(status).json({ message });
  });

  if (process.env.NODE_ENV === "production") {
    serveStatic(app);
  } else {
    const { setupVite } = await import("./vite");
    await setupVite(httpServer, app);
  }

  const port = parseInt(process.env.PORT || "5000", 10);
  httpServer.listen(
    {
      port,
      host: "0.0.0.0",
      reusePort: true,
    },
    () => {
      log(`serving on port ${port}`);
    },
  );
})();
